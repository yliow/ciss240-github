#include <iostream>

int main()
{
    std::cout << "42 + 1 = " << 42 + 1 << '\n'
              << "42 - 1 = " << 42 - 1 << '\n'
              << "42 * 1 = " << 42 * 1 << '\n';

    std::cout << "4 / 2 = " << 4 / 2 << '\n';
    std::cout << "100 / 20 = " << 100 / 20 << '\n';
    std::cout << "9 / 3 = " << 9 / 3 << '\n';
    std::cout << "8 / 3 = " << 8 / 3 << '\n';
    std::cout << "7 / 3 = " << 7 / 3 << '\n';

    //std::cout << "8.0 / 3.0 = " << 8.0 / 3.0 << '\n';

    std::cout << "2 / 0 = " << 2 / 0 << '\n';

    return 0;
}



